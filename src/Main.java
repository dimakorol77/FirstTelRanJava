public class Main {
    public static void main(String[] args) {
        //с новой строки каждое слово
        System.out.println("Hello,");
        System.out.println("my");
        System.out.println("name");
        System.out.println("is");
        System.out.println("Dima Korol");
        // в одну строку все слова
        System.out.print("Hello, my name is Dima Korol" +'\n');

        // или так еще можно
        System.out.print("Hello,");
        System.out.print(" my");
        System.out.print(" name");
        System.out.print(" is");
        System.out.print(" Dima Korol");
    }
}